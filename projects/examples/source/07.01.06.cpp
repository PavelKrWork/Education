#include <tuple>

//////////////////////////////////////////////////////

struct Variant 
{
	union 
	{ 
		int x, y = 0;
	};

//  -----------------------
	
	bool has_error = false;
};

//////////////////////////////////////////////////////

auto make_variant(int x)
{
	if (x > 0)
	{
		return Variant { .x = x, .has_error = false };
	}
	else
	{
		return Variant { .y = x, .has_error = true  };
	}
}

//////////////////////////////////////////////////////

int main()
{
	std::ignore = make_variant(1);
}

