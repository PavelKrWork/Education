////////////////////////////////////////////////////////////////////

#include <iostream>

////////////////////////////////////////////////////////////////////

template < typename T, typename ... Ts > void handle(T x, Ts ... ys)
{
	std::cout << "handle : x = " << x << ' ';
	
	std::cout << "sizeof...(ys) = " << sizeof...(ys) << '\n';

	if constexpr (sizeof...(ys) > 0) 
	{
		handle(ys...);
	}
}

////////////////////////////////////////////////////////////////////

int main()
{
	handle(1, 2, 3);
}

