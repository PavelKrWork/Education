#include <cassert>
#include <cstddef>
#include <iostream>
#include <span>
#include <string>
#include <vector>

/////////////////////////////////////////////////////////////////////

void test_v1(int x, int * y, int & z) 
{ 
	++x, ++(*y), ++z;
}

/////////////////////////////////////////////////////////////////////

void test_v2(int *, std::size_t   ) { std::cout << "test_v2 (1)\n"; }

void test_v2(std::span < int >    ) { std::cout << "test_v2 (2)\n"; }

void test_v2(std::string const &  ) { std::cout << "test_v2 (3)\n"; }

void test_v2(std::vector < int > &) { std::cout << "test_v2 (4)\n"; }

/////////////////////////////////////////////////////////////////////

int main()
{
    auto x = 1, y = 2, z = 3;

//  -----------------------------------------------
	
	test_v1(x, &y, z);

//  -----------------------------------------------

	assert(x == 1 && y == 3 && z == 4);

//  -----------------------------------------------

	auto size = 5uz;

//  -----------------------------------------------

	auto array = new int[size]{ 1, 2, 3, 4, 5 };

//  -----------------------------------------------

	test_v2(array, size);
    
//  -----------------------------------------------

	test_v2(std::span < int > (array, size));

//  -----------------------------------------------

	delete[] array;

//  -----------------------------------------------

	std::string string = "aaaaa";

//  -----------------------------------------------

	test_v2(string);

//  -----------------------------------------------

	std::vector < int > vector = { 1, 2, 3, 4, 5 };

//  -----------------------------------------------

	test_v2(vector);
}

