///////////////////////////////////////////////////////////////////

#include <cassert>
#include <source_location>
#include <string>

///////////////////////////////////////////////////////////////////

using namespace std::literals;

///////////////////////////////////////////////////////////////////

int main()
{
    constexpr auto location = std::source_location::current();

//  ---------------------------------------------------------------

    std::string file = location.file_name(), path = "06.04.cpp";

//  ---------------------------------------------------------------

    assert(file.substr(std::size(file) - std::size(path)) == path);

//  ---------------------------------------------------------------

    assert(location.line() == 16);

//  ---------------------------------------------------------------

    assert(location.function_name() == "int main()"s);
}

