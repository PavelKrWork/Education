//////////////////////////////////////////////////////////////////

#include <iostream>
#include <type_traits>

//////////////////////////////////////////////////////////////////

void test()
{ 
	std::cout << "test\n";
}

//////////////////////////////////////////////////////////////////

void invoke(void(*function)()) 
{ 
	std::cout << "invoke\n";
	
	function();
}

//////////////////////////////////////////////////////////////////

int main()
{
	static_assert(std::is_same_v < decltype( test), void   () > );

	static_assert(std::is_same_v < decltype(&test), void(*)() > );

//  --------------------------------------------------------------

	invoke(test);
}

