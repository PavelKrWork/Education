///////////////////////////////////////////////////////////////////////

#include <iostream>

///////////////////////////////////////////////////////////////////////

void handle_v1() 
{
	std::cout << "handle_v1\n";
}

///////////////////////////////////////////////////////////////////////

template < typename T, typename ... Ts > void handle_v1(T x, Ts ... ys)
{
	std::cout << "handle_v1 : x = " << x << ' ';
	
	std::cout << "sizeof...(ys) = " << sizeof...(ys) << '\n';
	
	handle_v1(ys...); // support : cppinsights.io
}

///////////////////////////////////////////////////////////////////////

template < typename T > void handle_v2(T x)
{
	std::cout << "handle_v2 : x = " << x << ' ';
}

///////////////////////////////////////////////////////////////////////

template < typename T, typename ... Ts > void handle_v2(T x, Ts ... ys)
{
	handle_v2(x);

	std::cout << "sizeof...(ys) = " << sizeof...(ys) << '\n';

	handle_v2(ys...);

	if (sizeof...(ys) == 1)
	{
		std::cout << "sizeof...(ys) = 0\n";
	}
}

///////////////////////////////////////////////////////////////////////

template < typename ... Ts > void handle_v3(Ts ... xs) 
{ 
	handle_v1(xs + xs...); // support : cppinsights.io
}

///////////////////////////////////////////////////////////////////////

struct Entity 
{ 
	int x = 0, y = 0;
};

///////////////////////////////////////////////////////////////////////

template < typename ... Ts > auto make_entity(Ts ... xs)
{
	return new Entity(xs...);
}

///////////////////////////////////////////////////////////////////////

int main()
{
	handle_v1(1, 2, 3);
		
	handle_v2(1, 2, 3);

	handle_v3(1, 2, 3);

//  -------------------------

    delete make_entity(1, 1);
}

