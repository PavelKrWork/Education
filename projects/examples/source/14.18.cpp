/////////////////////////////////////////////////////////////////////////////////

#include <mutex>
#include <utility>

/////////////////////////////////////////////////////////////////////////////////

class Entity
{
public :

    Entity(int x) : m_x(x) {}

//  -----------------------------------------------------------------------------

    void swap(Entity & other)
    {
        std::scoped_lock < std::mutex, std::mutex > lock(m_mutex, other.m_mutex);

        std::swap(m_x, other.m_x);
    }

private :

    int m_x = 0;
    
//  -----------------------------------------------------------------------------

    mutable std::mutex m_mutex;
};

/////////////////////////////////////////////////////////////////////////////////

int main()
{
    Entity entity_1(1);

    Entity entity_2(2);

//  ------------------------

    entity_1.swap(entity_2);
}

