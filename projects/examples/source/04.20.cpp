#include <print>

//////////////////////////////////////////////////////////////////

template < typename T, typename ... Ts > void test(T x, Ts ... ys)
{
	std::print("test : x = {} ", x);
	
	std::print("sizeof...(ys) = {}\n", sizeof...(ys));

	if constexpr (sizeof...(ys) > 0) 
	{
		test(ys...);
	}
}

//////////////////////////////////////////////////////////////////

int main()
{
	test(1, 2, 3);
}

