#include <print>

//////////////////////////////////////////////////////

void show(auto x, auto ... ys)
{
	std::print("show : x = {} ", x);
	
	std::print("sizeof...(ys) = {}\n", sizeof...(ys));

	if constexpr (sizeof...(ys) > 0) 
	{
		show(ys...);
	}
}

//////////////////////////////////////////////////////

int main()
{
	show(1, 2.0, "aaaaa");
}

