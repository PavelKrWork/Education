///////////////////////////////////////////////////////////////////////////////

#include <set>

///////////////////////////////////////////////////////////////////////////////

struct Entity 
{ 
	int x = 0;
};

///////////////////////////////////////////////////////////////////////////////

class Less
{
public :

	auto operator()(Entity const & lhs, Entity const & rhs) const
	{
		return lhs.x < rhs.x;
	}
};

///////////////////////////////////////////////////////////////////////////////

int main()
{
	std::set < Entity, Less > entities = { { 1 }, { 2 }, { 3 }, { 4 }, { 5 } };
}

