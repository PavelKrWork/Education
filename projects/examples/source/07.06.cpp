#include <tuple>

//////////////////////////////////////////////////////////

struct Alternative 
{
	union 
	{ 
		int x, y = 0;
	};

//  -----------------------
	
	bool has_error = false;
};

//////////////////////////////////////////////////////////

auto make_alternative(int x)
{
	if (x > 0)
	{
		return Alternative { .x = x, .has_error = false };
	}
	else
	{
		return Alternative { .y = x, .has_error = true  };
	}
}

//////////////////////////////////////////////////////////

int main()
{
	std::ignore = make_alternative(1);
}

