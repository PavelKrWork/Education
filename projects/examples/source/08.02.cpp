#include <cassert>

/////////////////////////////////////////

int main()
{
    assert((0x1234 &  0x00ff) == 0x0034);

    assert((0x1234 & ~0x00ff) == 0x1200);

    assert((0x1234 |  0x00ff) == 0x12ff);

    assert((0x1234 ^  0x00ff) == 0x12cb);

    assert((0x1200 >> 0x0008) == 0x0012);

    assert((0x0034 << 0x0008) == 0x3400);

//  -------------------------------------

    auto x = 1, y = 2;

//  -------------------------------------

    x ^= y;

    y ^= x;
        
    x ^= y;

//  -------------------------------------
        
    assert(x == 2 && y == 1);
}

